/**
 *	from JavaScript Switch: Exercise 1 ( https://www.w3schools.com/js/exercise_js.asp?filename=exercise_js_switch1 )
 *
 *	question:
 *	Create a switch statement that will alert "Hello" if fruits is "banana", and "Welcome" if fruits is "apple".
 *
 *	______(fruits) {
 *		____ "Banana":
 *			alert("Hello")
 *			break;
 *		____ "Apple":
 *			alert("Welcome")
 *			break;
 *	}
 *
 */
switch(fruits) {
	case "Banana":
		alert("Hello")
		break;
	case "Apple":
		alert("Welcome")
		break;
}