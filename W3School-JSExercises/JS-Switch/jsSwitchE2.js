/**
 *	from JavaScript Switch: Exercise 2 ( https://www.w3schools.com/js/exercise_js.asp?filename=exercise_js_switch2 )
 *
 *	question:
 *	Add a section that will alert("Neither") if fruits is neither "banana" nor "apple".
 *
 *	switch(fruits) {
 *		case "Banana":
 *			alert("Hello")
 *			break;
 *		case "Apple":
 *			alert("Welcome")
 *			break;
 *		________
 *			alert("Neither");
 *	}
 *
 */
switch(fruits) {
	case "Banana":
		alert("Hello")
		break;
	case "Apple":
		alert("Welcome")
		break;
	default:
		alert("Neither");
}